    private void tampilData() {
        DefaultTableModel model = new DefaultTableModel() {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false; // semua sel tidak bisa diubah langsung
        }
    };
        model.addColumn("ID");
        model.addColumn("Nama");
        model.addColumn("Kategori");
        model.addColumn("Harga");
        model.addColumn("Ukuran");

// load data dari DB
        try (
                Connection conn = Conec.getConnection(); Statement st = conn.createStatement(); ResultSet rs = st.executeQuery("SELECT * FROM dataminuman ORDER BY id_minuman")) {

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_minuman"),
                    rs.getString("nama"),
                    rs.getString("kategori"),
                    rs.getInt("harga"),
                    rs.getString("ukuran")
                });
            }

            jtab.setModel(model); // tampilkan ke JTable
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal load data: " + e.getMessage());
        }

    }

