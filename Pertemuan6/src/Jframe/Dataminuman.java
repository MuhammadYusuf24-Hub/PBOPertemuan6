    private void bdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bdActionPerformed
        // TODO add your handling code here:
        int row = jtab.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang mau dihapus!");
            return;
        }

        // ambil data dari tabel
        String id = jtab.getValueAt(row, 0).toString();
        String nama = jtab.getValueAt(row, 1).toString();
        String kategori = jtab.getValueAt(row, 2).toString();
        String harga = jtab.getValueAt(row, 3).toString();
        String ukuran = jtab.getValueAt(row, 4).toString();

        // tampilkan ke dialog delete
        tid2.setText(id);
        tnama2.setText(nama);
        cbka2.setSelectedItem(kategori);
        tha2.setText(harga);
        cbu2.setSelectedItem(ukuran);

        // semua field tidak bisa diedit
        tid2.setEditable(false);
        tnama2.setEditable(false);
        cbka2.setEnabled(false);
        tha2.setEditable(false);
        cbu2.setEnabled(false);

        DeleteDialog.pack();
        DeleteDialog.setLocationRelativeTo(this);
        DeleteDialog.setVisible(true);


    }//GEN-LAST:event_bdActionPerformed

